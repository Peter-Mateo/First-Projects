public class ifConditional{
    public static void main(String[] args){
        int number5 = 5;
        switch(number5){
            case 1:
                System.out.println("Yaga");
                break;
            case 2:
                System.out.println("Baby Yaga");
                break;
            case 3:
                System.out.println("Yaga Yaga");
                break;
            case 4:
                System.out.println("yaga");
                break;
            case 5:
                System.out.println("ooooh");
                break;
            case 6:
                System.out.println("this ain't it chief");
                break;
            default:
                System.out.println("Default Baby");
        }
        }
    }